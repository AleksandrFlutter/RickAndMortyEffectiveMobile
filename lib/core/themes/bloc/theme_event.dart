part of 'theme_bloc.dart';

/// Базовый абстрактный класс для всех событий, связанных с управлением темой приложения
/// Помечен как @immutable для обеспечения неизменяемости событий (рекомендация BLoC)
@immutable
abstract class ThemeEvent {}

/// Событие изменения темы пользователем
/// Вызывается при переключении тумблера "Тёмная тема" в настройках
class ThemeChanged extends ThemeEvent {
  /// Новое состояние темы: true — тёмная тема включена, false — выключена
  final bool isDark;

  ThemeChanged(this.isDark);
}

/// Событие инициализации темы при запуске приложения
/// Используется для загрузки сохранённого режима темы из локальной базы данных
class ThemeInit extends ThemeEvent {}